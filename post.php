<h3>MY LightSchool <small><span style="color: gray">LightSchool Alfa Preview 1</span></small></h3>
<p><img src="//images.lightschool.it/mybrowser.png" style="width: 100%" /></p>
<p>Veste grafica rivista e moltissime nuove funzioni, la pi&ugrave; grande rivoluzione di LightSchool. Ecco alcuni cambiamenti:
  <ul>
    <li>La pagina di accesso ora &egrave; dinamica e non si ricarica pi&ugrave; quando si sbagliano i dati di accesso</li>
    <li>La pagina di registrazione ora &egrave; dinamica e avvisa senza cambiare pagina eventuale presenza di errori</li>
    <li>Risolto un problema raro che impediva di attivare l'account in quanto non si riceveva l'e-mail di attivazione</li>
    <li>Recupero password ora &egrave; dinamico</li>
    <li>La barra in basso (taskbar) &egrave; ora personalizzabile. Si pu&ograve; scegliere quali icone mostrare, la dimensione della barra e se spostarla in basso, a destra o a sinistra.</li>
    <li>&Egrave; stato aggiunto un pulsante all'inizio della taskbar con la propria foto profilo che apre il menu principale dal quale si possono avviare e aggiungere applicazioni che non sono presenti sulla barra.</li>
    <li>Il "sottotitolo" delle cartelle ora comprende il numero degli elementi contenuti dalla cartella</li>
    <li>Il menu contestuale (tasto destro) sui file ora &egrave; animato</li>
    <li>Si possono selezionare pi&ugrave; file contemporanemanete per spostarli, condividerli o eliminarli tutti insieme</li>
    <li>Aggiunta la possibilit&agrave; di condividere quaderni con un link univoco anche per persone non iscritte a LightSchool</li>
    <li>I quaderni possono ora essere esportati in file Word e PDF</li>
    <li>La barra laterale destra ora comprende un codice QR per aprire velocemente un quaderno da qualsiasi dispositivo</li>
    <li>I quaderni possono essere incorporati in altri siti web o blog</li>
    <li>Cronologia modifiche ora funziona meglio e permette di avere uno storico di tutte le modifiche apportate a un quaderno</li>
    <li>Si possono copiare e incollare quaderni</li>
    <li>La ricerca ora &egrave; istantanea e cerca nella pagina attuale, mostrando soltanto i risultati corrispondenti alla ricerca. Es: nei quaderni mostra soltanto i file contenenti la parola cercata, nelle impostazioni invece mostra solamente le impostazioni corrispondenti al termine di ricerca e cos&igrave; via</li>
    <li>Il caricamento dei file avviene ora in tempo reale, viene mostrata una barra riguardante il progresso e nel caso di immagini viene visualizzata anche un'anteprima</li>
    <li>LightSchool Writer (la pagina dedicata alla creazione di nuovi quaderni) comprende ora modelli dai quali si pu&ograve; partire (curriculum, compiti, tema, ecc)</li>
    <li>La pagina dei file condivisi &egrave; ora suddivisa per utente</li>
    <li>LightSchool Test e Verifiche ora comprende un editor di creazione molto pi&ugrave; dinamico e completo: si possono scegliere domande aperte, chiuse a risposta multipla o singola, grafiche, ascolto e a completamento</li>
    <li>Il registro ora permette di caricare una foto di classe, di gestire le giustificazioni per assenze, ritardi ed uscite anticipate</li>
    <li>&Egrave; possibile aggiungere le classi preferite al menu contestuale dell'icona del registro sulla taskbar</li>
    <li>Integrato un'innovativo meccanismo di appello</li>
    <li>Diario ora &egrave; in grado di organizzare le materie asssegnano un colore univoco e gestire promemoria via e-mail e notifiche in app</li>
    <li>Orario ora gestisce le materie per colore univoco (come Diario)</li>
    <li>I messaggi ora vengono raggruppati per mittente esattamente come accade su Whatsapp</li>
    <li>Si possono bloccare gli utenti indesiderati</li>
    <li>Rubrica ora richiede che il nome utente del contatto che si deve aggiungere sia corretto e corrisponda a un utente esistente su LightSchool</li>
    <li>Sono presenti le foto profilo sia in messaggi che in rubrica corrispondenti all'utente</li>
    <li>Si possono creare gruppi per condividere velocemente con tutti i membri o inviare messaggi a tutti i membri del gruppo</li>
    <li>I contatti e i gruppi possono essere condivisi con altri utenti</li>
    <li>Integrato un sistema di ricerca utenti</li>
    <li>In impostazioni si pu&ograve; scegliere la propria regione in modo che Diario mostri automaticamente le vacanze disposte dalla tua regione di appartenenza. <span style="color: gray">Solo in Italia</span></li>
    <li>Il selettore del colore preferito non &egrave; pi&ugrave; libero ma al contrario c'&egrave; una gamma di 81 colori predefiniti</li>
    <li>&Egrave; possibile impostare uno sfondo visibile in tutte le pagine e regolarne la trasparenza per un migliore contrasto con i contenuti della pagina</li>
    <li>Si pu&ograve; cambiare il set di icone del sito</li>
    <li>Gli avvisi visibili solo una volta come aiuti e suggerimenti possono essere resi nuovamente visibili reimpostando gli avvisi dalle impostazioni del sito</li>
    <li>La pagina dedicata all'elenco dei dispositivi connessi ora &egrave; organizzata in base alla tipologia di dispositivo collegato (PC, tablet o smartphone)</li>
    <li>&Egrave; possibile personalizzare la tipologia di notifiche da ricevere e il canale preferito fra e-mail e notifiche in app</li>
    <li>Al momento del cambio password &egrave; possibile scegliere di disconnettere tutti i dispositivi attualmente collegati ad eccezione del dispositivo attuale</li>
    <li>Molti altri miglioramenti "sotto il cofano"</li>
  </ul>
</p>
